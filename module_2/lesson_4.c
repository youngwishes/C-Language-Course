// Урок №2.4. Функция printf() для форматированного вывода

#include <stdio.h>

int main(void)
{
    printf("=== ОСНОВЫ PRINTF() ===\n");

    // Простой вывод текста
    printf("Привет, мир!\n");

    // Вывод переменных
    int number = 42;
    float price = 99.99;
    char letter = 'A';

    printf("Число: %d\n", number);      // %d - для целых чисел
    printf("Цена: %.2f\n", price);      // %f - для дробных чисел
    printf("Буква: %c\n", letter);      // %c - для символов

    printf("\n=== СПЕЦИФИКАТОРЫ ФОРМАТА ===\n");

    // Разные спецификаторы
    int age = 25;
    float temp = -5.5;
    char grade = 'B';

    printf("Возраст: %d лет\n", age);
    printf("Температура: %f градусов\n", temp);
    printf("Оценка: %c\n", grade);

    printf("\n=== ФОРМАТИРОВАНИЕ ЧИСЕЛ ===\n");

    // Форматирование дробных чисел
    float pi = 3.14159;
    printf("Пи: %f\n", pi);           // стандартный вывод
    printf("Пи: %.2f\n", pi);         // 2 знака после запятой
    printf("Пи: %.3f\n", pi);         // 3 знака после запятой

    // Вывод нескольких переменных
    int x = 10, y = 20;
    printf("x = %d, y = %d, сумма = %d\n", x, y, x + y);

    printf("\n=== ВЫВОД ТЕКСТА И ЧИСЕЛ ===\n");
    
    // Комбинированный вывод
    printf("Ученик получил %d баллов из %d возможных\n", 85, 100);
    printf("Средний балл: %.1f\n", 4.75);
    
    return 0;
}